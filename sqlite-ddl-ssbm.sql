CREATE TABLE LINEORDER (
  LO_ORDERKEY INTEGER,
  LO_LINENUMBER INTEGER,
  LO_CUSKEY INTEGER,
  LO_PARTKEY INTEGER,
  LO_SUPPKEY INTEGER,
  LO_ORDERDATE INTEGER,
  LO_ORDERPRIORITY TEXT,
  LO_SHIPPRIORITY TEXT,
  LO_QUANTITY INTEGER,
  LO_EXTENDEDPRICE REAL,
  LO_ORDTOTALPRICE REAL,
  LO_DISCOUNT REAL,
  LO_REVENUE REAL,
  LO_SUPPLYCOST REAL,
  LO_TAX REAL,
  LO_COMMITDATE INTEGER,
  LO_SHPIMODE TEXT,
  PRIMARY KEY(LO_ORDERKEY, LO_LINENUMBER)
);

CREATE TABLE PART (
  P_PARTKEY TEXT,
  P_NAME TEXT,
  P_MFGR TEXT,
  P_CATEGORY TEXT,
  P_BRAND1 TEXT,
  P_COLOR TEXT,
  P_TYPE TEXT,
  P_SIZE INTEGER,
  P_CONTAINER TEXT,
  PRIMARY KEY(P_PARTKEY)
);

CREATE TABLE SUPPLIER (
  S_SUPPKEY INTEGER,
  S_NAME TEXT,
  S_ADDRESS TEXT,
  S_CITY TEXT,
  S_NATION_PREFIX INTEGER,
  S_NATION TEXT,
  S_REGION TEXT,
  S_PHONE TEXT,
  PRIMARY KEY(S_SUPPKEY)
);

CREATE TABLE CUSTOMER (
  C_CUSTKEY INTEGER,
  C_NAME TEXT,
  C_ADDRESS TEXT,
  C_CITY TEXT,
  C_NATION_PREFIX INTEGER,
  C_NATION TEXT,
  C_REGION TEXT,
  C_PHONE TEXT,
  C_MKTSEGMENT TEXT,
  PRIMARY KEY(C_CUSTKEY)
);

CREATE TABLE DATE (
  D_DATEKEY INTEGER,
  D_DATE TEXT,
  D_DAYOFTHEWEEK TEXT,
  D_MONTH TEXT,
  D_YEAR INTEGER,
  D_YEARMONTHNUM INTEGER,
  D_YEARMONTH TEXT,
  D_DAYNUMINWEEK INTEGER,
  D_DAYNUMINMONTH INTEGER,
  D_DAYNUMINYEAR INTEGER,
  D_MONTHNUMINYEAR INTEGER,
  D_WEEKNUMINYEAR INTEGER,
  D_SELLINGSEASON TEXT,
  D_LASTDAYINWEEKFL INTEGER,
  D_LASTDAYINMONTHFL INTEGER,
  D_HOLIDAYFL INTEGER,
  D_WEEKDAYFL INTEGER,
  PRIMARY KEY(D_DATEKEY)
);
